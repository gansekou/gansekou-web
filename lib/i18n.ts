export function normalizeLanguage(language?: string | null): Language {
  return language?.toUpperCase() === "EN" ? "EN" : "FR";
}

export function detectBrowserLanguage(): Language {
  if (typeof navigator === "undefined") return "FR";
  return navigator.language.toLowerCase().startsWith("en") ? "EN" : "FR";
}

export function resolveLanguage(user?: Pick<User, "preferred_language"> | null): Language {
  if (user?.preferred_language) return normalizeLanguage(user.preferred_language);
  if (typeof window !== "undefined") {
    const stored = window.localStorage.getItem(languageStorageKey);
    if (stored) return normalizeLanguage(stored);
  }
  return detectBrowserLanguage();
}

export function setStoredLanguage(language: Language) {
  if (typeof window === "undefined") return;
  window.localStorage.setItem(languageStorageKey, language);
  window.dispatchEvent(new CustomEvent("gansekou-language-change", { detail: language }));
}

export function t(key: AnyTranslationKey, language: Language) {
  const extended = extendedTranslations[language][key] || extendedTranslations.FR[key];
  const base = dictionaries[language] as Record<string, string>;
  const fallback = dictionaries.FR as Record<string, string>;
  return base[key] || fallback[key] || extended || key;
}

